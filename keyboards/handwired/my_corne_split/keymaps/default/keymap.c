#define LED_PIN GP17
void matrix_init_user(void) {
    setPinOutput(LED_PIN);
    writePinLow(LED_PIN); // Turn the LED off initially
}

// Toggle LED
bool led_state = false;
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case KC_CAPS:
            if (record->event.pressed) {
               led_state = !led_state;
               if(led_state)
               {
                  writePinHigh(LED_PIN);
               } else {
                  writePinLow(LED_PIN);
               }
            }
            break;
    }
    return true;
}


